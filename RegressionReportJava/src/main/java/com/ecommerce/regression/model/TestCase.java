package com.ecommerce.regression.model;

public class TestCase {
    private String name;
    private String feature;
    private String scenario;
    private String step;
    private String result;
    private String remarks;
    private String testFailureClassname;
    private String testFailureMessage;
    private String testFailureSummary;
    private String githubJobId;
    private String branch;
    private String brand;
    private String team;
    private String workflowName;
    private String env;
    private String screenshot;

    // Getters and setters
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    public String getFeature() { return feature; }
    public void setFeature(String feature) { this.feature = feature; }
    public String getScenario() { return scenario; }
    public void setScenario(String scenario) { this.scenario = scenario; }
    public String getStep() { return step; }
    public void setStep(String step) { this.step = step; }
    public String getResult() { return result; }
    public void setResult(String result) { this.result = result; }
    public String getRemarks() { return remarks; }
    public void setRemarks(String remarks) { this.remarks = remarks; }
    public String getTestFailureClassname() { return testFailureClassname; }
    public void setTestFailureClassname(String testFailureClassname) { this.testFailureClassname = testFailureClassname; }
    public String getTestFailureMessage() { return testFailureMessage; }
    public void setTestFailureMessage(String testFailureMessage) { this.testFailureMessage = testFailureMessage; }
    public String getTestFailureSummary() { return testFailureSummary; }
    public void setTestFailureSummary(String testFailureSummary) { this.testFailureSummary = testFailureSummary; }
    public String getGithubJobId() { return githubJobId; }
    public void setGithubJobId(String githubJobId) { this.githubJobId = githubJobId; }
    public String getBranch() { return branch; }
    public void setBranch(String branch) { this.branch = branch; }
    public String getBrand() { return brand; }
    public void setBrand(String brand) { this.brand = brand; }
    public String getTeam() { return team; }
    public void setTeam(String team) { this.team = team; }
    public String getWorkflowName() { return workflowName; }
    public void setWorkflowName(String workflowName) { this.workflowName = workflowName; }
    public String getEnv() { return env; }
    public void setEnv(String env) { this.env = env; }
    public String getScreenshot() { return screenshot; }
    public void setScreenshot(String screenshot) { this.screenshot = screenshot; }
}
